#!/bin/bash

# Простой скрипт для поиска фиксированного слова в фиксированном файле

FILENAME="config.txt"
SEARCH_WORD="path"

echo "--- Поиск слова '$SEARCH_WORD' в файле '$FILENAME' (простой Bash) ---"

# Проверяем, существует ли файл перед тем как его читать
if [ ! -f "$FILENAME" ]; then
  echo "Ошибка: Файл '$FILENAME' не найден."
  exit 1 # Выход с кодом ошибки
fi

# Используем grep для поиска строк, содержащих слово
# Опция --color=auto подсветит найденное слово, если терминал поддерживает
grep --color=auto "$SEARCH_WORD" "$FILENAME"

echo "--- Поиск завершен ---"